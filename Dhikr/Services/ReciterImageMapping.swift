    /// Returns the Firebase Storage URL for a reciter's profile image
    /// - Parameter reciterId: The mp3quran reciter ID
    /// - Returns: URL to the image, or nil if no image exists for this reciter
    static func imageURL(for reciterId: Int) -> URL? {
        guard let filename = idToFilename[reciterId] else {
            return nil
        }

        // URL-encode the filename (Arabic characters need encoding)
        guard let encodedFilename = filename.addingPercentEncoding(withAllowedCharacters: .urlPathAllowed) else {
            return nil
        }

        let urlString = "\(baseURL)\(encodedFilename)?alt=media"
        return URL(string: urlString)
    }

    /// Check if a reciter has an available image
    static func hasImage(for reciterId: Int) -> Bool {
        return idToFilename[reciterId] != nil
    }

    /// Total number of reciters with images
    static var count: Int {
        return idToFilename.count
    }
